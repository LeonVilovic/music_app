    private void UlogujSe() {
        String username = UserTextField.getText().trim();
        String password = String.valueOf(LogInPasswordField.getPassword());
        try {
            Korisnik k = CommunicationController.getInstance().logIn(username, password);
            JOptionPane.showMessageDialog(this, "Welcome " + k.getIme() + " " + k.getPrezime(), "Uspesan login", JOptionPane.PLAIN_MESSAGE);
            this.dispose();
            JFrame f = new MainFrame();

            f.setTitle("User: " + k.getIme());
            f.setVisible(true);

        } catch (Exception ex) {
            Logger.getLogger(LogInFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Gre≈°ka prilikom prijavljivanja na sistem!", JOptionPane.WARNING_MESSAGE);
        }
        
    }
    
    
    
