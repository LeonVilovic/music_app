    private void saveKompozicijajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveKompozicijajButtonActionPerformed
        if (selektovaniKorisnik != null) {
            try {
                Kompozicija kompozicija = new Kompozicija();
                kompozicija.setNaziv(nazivKompozicijeTextField.getText());
                kompozicija.setKorisnikID(selektovaniKorisnik);
                GregorianCalendar datum = new GregorianCalendar();
                kompozicija.setUbaziOd(datum);

                TableModelZvucniZapis modelZvucniZapis = (TableModelZvucniZapis) ZvucniZapisjTable.getModel();
                TableModelPartitura modelPartitura = (TableModelPartitura) PartiturejTable.getModel();

                CommunicationController.getInstance().saveKompozicija(kompozicija, modelPartitura.getAllPartiture(), modelZvucniZapis.getAllZvucniZapis(), modelZvucniZapis.getAllIzvodjac());
                JOptionPane.showMessageDialog(this, "Sistem je sacuvao kompoziciju", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                Logger.getLogger(KompozicijaFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Izaberite Kompozitora", "Obavestenje", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_saveKompozicijajButtonActionPerformed

    private void dodajPartituruButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodajPartituruButtonActionPerformed

        try {
            validacijaPartitura();

            Partitura p = new Partitura();
            p.setPutDoFajla(putDoFajlaPartituraTextField.getText().replace("\\", "\\\\"));
            p.setCena(Double.valueOf(cenaPartituraTextField.getText()));
            p.setValuta(valutaPartituraTextField.getText());
            p.setFormat(formatPartituraTextField.getText());
            TableModelPartitura model = (TableModelPartitura) PartiturejTable.getModel();

            model.addPartitura(p);
            model.fireTableDataChanged();
        } catch (NumberFormatException ex) {
            Logger.getLogger(KompozicijaFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Cena mora biti broj: " + ex.getMessage(), "Paznja", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(KompozicijaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_dodajPartituruButtonActionPerformed

    private void dodajZvucniZapisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodajZvucniZapisButtonActionPerformed
        ZvucniZapis z = new ZvucniZapis();
        Izvodjac i = new Izvodjac();

        try {
            validacijaZvucniZapis();

            z.setDuzina(Integer.valueOf(duzinaZvucniZapisTextField.getText()));
            z.setCena(Double.valueOf(cenaZvucniZapisjTextField.getText()));
            z.setFormat(formatZvucniZapisTextField.getText());
            z.setPutDoFajla(putDoFajlaZvucniZapisjTextField.getText().replace("\\", "\\\\"));
            z.setValuta(valutaZvucniZapisjTextField.getText());
            i.setNaziv(izvodjacZvucniZapisjTextField.getText());

            TableModelZvucniZapis model = (TableModelZvucniZapis) ZvucniZapisjTable.getModel();
            model.addZvucniZapisSaIzvodjacem(z, i);
            model.fireTableDataChanged();

        } catch (NumberFormatException ex) {
            Logger.getLogger(KompozicijaFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Duzina mora biti ceo broj, Cena mora biti broj: " + ex.getMessage(), "Paznja", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(KompozicijaFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Paznja", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_dodajZvucniZapisButtonActionPerformed

    private void searchKompozicijaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchKompozicijaButtonActionPerformed
        try {

            List<OpstiDomenskiObjekat> ListaODO = CommunicationController.getInstance().searchKompozicija(new Kompozicija(), searchKompozicijaTextField.getText());
            List<Kompozicija> kompozicije = new ArrayList<>();
            List<Partitura> partiture = new ArrayList<>();
            List<ZvucniZapis> zvucniZapisi = new ArrayList<>();
            List<Izvodjac> izvodjaci = new ArrayList<>();
            List<Korisnik> korisnikci = new ArrayList<>();

            for (int i = 0; i < ListaODO.size(); i = i + 5) {
                kompozicije.add((Kompozicija) ListaODO.get(i));
                partiture.add((Partitura) ListaODO.get(i + 1));
                zvucniZapisi.add((ZvucniZapis) ListaODO.get(i + 2));
                izvodjaci.add((Izvodjac) ListaODO.get(i + 3));
                korisnikci.add((Korisnik) ListaODO.get(i + 4));
            }

            TableModelSearchKompozicja model = new TableModelSearchKompozicja(kompozicije, partiture, zvucniZapisi, izvodjaci, korisnikci);
            model.fireTableStructureChanged();
            SearchKopozicijajTable.setModel(model);
            SearchKopozicijajTable.getColumnModel().getColumn(4).setPreferredWidth(28);

        } catch (Exception ex) {
            Logger.getLogger(KompozicijaFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska pri pretrazivanju kompozicija", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchKompozicijaButtonActionPerformed

    private void SearchKopozicijajTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SearchKopozicijajTableMouseClicked
        int selectedRow = SearchKopozicijajTable.getSelectedRow();
        if (selectedRow != -1) {
            TableModelSearchKompozicja model = (TableModelSearchKompozicja) SearchKopozicijajTable.getModel();
            List<OpstiDomenskiObjekat> listaODO = model.getKopozicijaDataAt(selectedRow);
            try {
                CommunicationController.getInstance().setSavedData(listaODO);
            } catch (IOException ex) {
                Logger.getLogger(UserFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            JPanel pannel = new KompozicijaDetailsPanel();
            JDialog dialog = new JDialog(this, "Detalji o kompoziciji", true);
            dialog.add(pannel);
            //   dialog.setLocationRelativeTo(this);
            dialog.setLocation(150, 150);
            dialog.pack();
            dialog.setVisible(true);
        }
    }//GEN-LAST:event_SearchKopozicijajTableMouseClicked

