    private void KorisniciTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_KorisniciTableMouseClicked
        int selectedRow = KorisniciTable.getSelectedRow();
        if (selectedRow != -1) {
            TableModelKorisnik model = (TableModelKorisnik) KorisniciTable.getModel();
            Korisnik k = model.getKorisnikAt(selectedRow);
            selektovaniKorisnik = k;
            jTextField1.setText(k.getID());
            jTextField2.setText(k.getIme());
            jTextField3.setText(k.getPrezime());
            jTextField4.setText(k.getClanOd().get(GregorianCalendar.DAY_OF_MONTH) + "-" + k.getClanOd().get(GregorianCalendar.MONTH) + "-" + k.getClanOd().get(GregorianCalendar.YEAR));
            jTextField5.setText(k.getUsername());
            jTextField6.setText(k.getEmail());
        }
    }//GEN-LAST:event_KorisniciTableMouseClicked

